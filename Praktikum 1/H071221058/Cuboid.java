class Cuboid {
    double height;
    double width;
    double length;

    double getVolume() {
        return height * width * length; 
    }
    public void height (double height) {
        this.height = height;
    }
    public void width (double width) {
        this.width = width;
    }
    public void length (double length) {
        this.length = length;
    }

    }

